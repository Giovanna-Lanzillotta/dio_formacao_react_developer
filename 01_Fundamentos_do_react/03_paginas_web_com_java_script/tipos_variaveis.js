// Tipos de Variáveis

// var
// Ele tem escopo de função, o que significa que se você criar um var dentro de um if ou um for, 
// ele "vaza" para fora e continua existindo em toda a função.
var nome = 'Pablo'
var nome = 'Giovanna'


// let
// Ele tem escopo de bloco, ou seja, se você criar um let dentro de um if, ele só existe ali dentro.
let idade = 25
idade = 30


// const
//Como o nome diz, serve para constantes. Assim como o let, ela tem escopo de bloco e não permite 
// redeclaração.
const aprovado = true


// Boolean //true e false
// null // nulo
// undefined // não definido
// Number // 42 ou 3.14159
