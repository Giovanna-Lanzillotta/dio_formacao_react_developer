// Arrow Functions

// Função
function subtrair(num1, num2){
    return num1 - num2;
}


const resultado = subtrair(10, 5)
console.log(resultado)



// Transformando em arrow functions
const somar =  (num1, num2) => {
    return num1 + num2;
}


const resultado_soma = somar(14, 6);
console.log(resultado_soma)



// Colocando em uma linha
const multiplicar = (num1, num2) => num1 * num2;

const resultado_multiplicacao = multiplicar(8, 5)
console.log(resultado_multiplicacao)